package com.student.model;

public class StudentModel {
	
	public StudentModel(int age, String username, String useremail, int sex, int id) {
		super();
		this.age = age;
		this.username = username;
		this.useremail = useremail;
		this.sex = sex;
		this.id = id;
	}
	private int age;
	private String username;
	private String useremail;
	private int sex;
	private int id;

}
